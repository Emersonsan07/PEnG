// --- FORM HANDLING (DEMO) ---
document.querySelector('.newsletter-form').addEventListener('submit', (e) => {
    e.preventDefault();
    const btn = e.target.querySelector('button');
    const originalText = btn.innerText;
    
    btn.innerText = "Subscribed!";
    btn.style.backgroundColor = "#fff";
    btn.style.color = "#00D084";
    
    setTimeout(() => {
        btn.innerText = originalText;
        btn.style.backgroundColor = "";
        btn.style.color = "";
        e.target.reset();
    }, 3000);
});

// LÓGICA DA CALCULADORA
function calculateImpact() {
    const volume = document.getElementById('gasInput').value;
    document.getElementById('gasValue').innerText = Number(volume).toLocaleString();

    // Fórmulas Fictícias (Baseadas em rácios comuns de GTL)
    const fuel = Math.round(volume * 0.52); // 0.52L por m³
    const co2 = (volume * 0.0025).toFixed(1); // Toneladas evitadas
    const revenue = Math.round(fuel * 1.6); // $1.60 CAD por litro

    document.getElementById('fuelResult').innerText = fuel.toLocaleString();
    document.getElementById('co2Result').innerText = co2;
    document.getElementById('revResult').innerText = "$" + revenue.toLocaleString();
}

// Iniciar cálculo ao carregar
calculateImpact();